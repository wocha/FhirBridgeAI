def claim_next_job(session: Session, worker_id: str) -> Job | None:
    """Atomically finds and locks a job for this worker."""
    now = datetime.now(UTC)

    # Find a job that is:
    # 1. Not completed and not permanently failed
    # 2. lock_id is NULL (available) OR next_retry_at is fully reached
    query = select(Job).where(
        Job.status.notin_([JobStatus.FHIR_GENERATED, JobStatus.ERROR, JobStatus.FAILED_PERMANENTLY]),
        or_(
            Job.lock_id == None,
            and_(Job.next_retry_at != None, Job.next_retry_at <= now)
        )
    ).order_by(Job.created_at.asc()).with_for_update()  # if dialect supports it natively

    job = session.execute(query).scalars().first()

    if job:
        job.lock_id = worker_id
        job.locked_at = now
        session.commit()
        return job

    return None


async def run_worker_loop() -> None:
    """Main execution loop for the autonomous dispatcher."""
    worker_id = str(uuid.uuid4())
    logger.info(f"Starting worker {worker_id}. Polling SQLite queue at {DB_PATH}...")

    # Ensure tables exist
    from schema_reference import Base
    Base.metadata.create_all(bind=engine)

    while True:
        with SessionLocal() as session:
            try:
                # Add 'and_' manually for the query inside 'claim_next_job' since we didn't import it at module scope
                # Re-writing the where-clause properly inside the session loop:
                now = datetime.now(UTC)
                query = select(Job).where(
                    Job.status.notin_([JobStatus.FHIR_GENERATED, JobStatus.FAILED_PERMANENTLY]),
                    or_(
                        Job.lock_id == None,
                        and_(Job.next_retry_at != None, Job.next_retry_at <= now)
                    )
                ).order_by(Job.created_at.asc())

                job = session.execute(query).scalars().first()
                if job:
                    job.lock_id = worker_id
                    job.locked_at = now
                    session.commit()
                    claimed_job_id = job.id
                else:
                    claimed_job_id = None

            except Exception as e:
                logger.error(f"Database error during polling: {e}")
                await asyncio.sleep(5)
                continue

        if not claimed_job_id:
            await asyncio.sleep(3) # Wait before polling again
            continue

        # Process the claimed job
        with JOB_PROCESSING_DURATION.time():
            with SessionLocal() as session:
                # Re-fetch local to this session
                job = session.get(Job, claimed_job_id)
                if not job:
                    continue

                try:
                    # Dispatch correctly
                    if job.status == JobStatus.PENDING:
                        job.status = JobStatus.OCR_PROCESSING
                        session.commit()

                    if job.status == JobStatus.OCR_PROCESSING:
                        await process_ocr(job, session)

                    if job.status == JobStatus.LLM_EXTRACTION:
                        await process_llm_extraction(job, session)

                    # If everything succeeded, release lock if it reached final state.
                    if job.status == JobStatus.FHIR_GENERATED:
                        job.lock_id = None
                        job.locked_at = None
                        session.commit()

                except Exception as e:
                    logger.error(f"Job {job.id} failed during {job.status}: {e}")

                    # Exponential backoff update
                    job.retry_count += 1
                    job.error_message = traceback.format_exc()

                    if job.retry_count >= MAX_RETRIES:
                        job.status = JobStatus.FAILED_PERMANENTLY
                        logger.critical(f"Job {job.id} failed permanently after {MAX_RETRIES} attempts.")
                    else:
                        # Exponential Backoff based on retries: 2^retry_count * 10 seconds
                        backoff_seconds = (2 ** job.retry_count) * 10
                        job.next_retry_at = datetime.now(UTC) + timedelta(seconds=backoff_seconds)
                        job.lock_id = None  # Release lock for future pickup
                        logger.info(f"Job {job.id} will retry at {job.next_retry_at} (Attempt {job.retry_count}/{MAX_RETRIES})")

                    session.commit()


if __name__ == "__main__":
    start_http_server(8000)
    logger.info("Prometheus metrics server started on port 8000")
    try:
        asyncio.run(run_worker_loop())
    except KeyboardInterrupt:
        logger.info("Worker gracefully shutting down.")
